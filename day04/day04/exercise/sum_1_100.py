#   3. 写程序 求:
#        1 + 2 + 3 + 4 + ..... + 99 + 100
#       的和,并打印结果:
#          5050
#       提示:
#         用一个变量初始值为0来累加结果

i = 1
while i <= 100:
    s = 0   # 创建一个变量用不准备累加数据
    # print(i, end=' ')
    s += i  # 将此时的i值累加到s中
    i += 1

# 当while结束,此时的s的值为 1 + 2 ... + 99 + 100
print("1+2+3+.....+99+100=", s)