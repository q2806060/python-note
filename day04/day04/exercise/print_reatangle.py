# 练习:
#   输入一个整数n, 打印一个宽度和高度都为n个字符的长方形
#   如: 
#     输入: 4
#   打印:
#     ####
#     #  #
#     #  #
#     ####
#   如: 
#     输入: 6
#   打印:
#     ######
#     #    #
#     #    #
#     #    #
#     #    #
#     ######

n = int(input("请输入一个大于0的整数:"))
# 打印第一行:
print("#" * n)

# 打印中间的n-2行
i = 0
while i < n - 2:
    # 此处的代码会执行n-2 次
    line = '#' + ' ' * (n - 2) + '#'
    print(line)
    i += 1

# 打印最后一行
if n > 1:
    print("#" * n)


