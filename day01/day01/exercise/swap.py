# 练习:
#    变量交换练习:
#      已知有两个变量
#         a 绑定 10000
#         b 绑定 20000
#      问: 在不创建任何新对象的情况下．如何让a 和 b
#         交换绑定的对象
#         a = 10000
#         b = 20000
#         ....
#         print(a)  # 20000
#         print(b)  # 10000


a = 10000  # 创建一个10000这样的数据对象,再创建一个变量a绑定
b = 20000
# 实现交换算法
t = a
a = b
b = t
print(a)  # 20000
print(b)  # 10000
