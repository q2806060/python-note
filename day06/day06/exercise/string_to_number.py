#   2. 已知有一个字符串:
#      s = "100,200,800,500,300" 
#      将其转化为列表，列表内都为整数:
#      L = [100, 200, 800, 500, 300]
#      求出L列表内部所有数字的和


s = "100,200,800,500,300"
temp = s.split(',')  # temp=['100','200','800','500','300']
L = [int(x) for x in temp]
print(L)
# L = [100, 200, 800, 500, 300]
print("所有数字的和是:", sum(L))

