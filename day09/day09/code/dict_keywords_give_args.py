

# 此示例示意字典关键字传参 
def myfun1(a, b, c):
    '''这是一个函数参数的示例'''
    print('a=', a)
    print('b=', b)
    print('c=', c)

d1 = {"c":33, "b":22, 'a':11}
myfun1(**d1)  # 拆解字典后进行关键字传参
myfun1(c=d1['c'], b=d1['b'], a=d1['a'])

